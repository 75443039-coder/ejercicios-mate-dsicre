# Datos del ejercicio
velocidad_inicial = 30  # en km/h
incremento_por_minuto = 5  # en km/h
tiempo_en_minutos = 10  # en minutos

# Cálculo de la velocidad final
velocidad_final = velocidad_inicial + (incremento_por_minuto * tiempo_en_minutos)

print(f"La velocidad del automóvil después de {tiempo_en_minutos} minutos es: {velocidad_final} km/h")
